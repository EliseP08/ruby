# #{} permet de remplacer dans une phrase #{} et ce qu'il y a dedans par le résultat directement

# Imprime sur l'écran la phrase entre ""
puts "On va compter le nombre d'heures de travail à THP"

# Imprime sur l'écran Travail : et le résultat de l'opération entre #{} (multiplication)
puts "Travail : #{10 * 5 * 11}"

# Imprime sur l'écran En minutes ça fait : et le résultat de l'opération entre #{} (multiplication)
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

# Imprime sur l'écran la phrase entre ""
puts "Et en secondes ?"

# Imprime sur l'écran le résultat de l'opération (multiplication)
puts 10 * 5 * 11 * 60 * 60

# Imprime sur l'écran la phrase entre ""
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

# Imprime sur l'écran le résultat de l'opération : true or false (ici false) (valeur inférieure à une autre)
puts 3 + 2 < 5 - 7

# Imprime sur l'écran Travail : et le résultat de l'opération entre #{} (addition)
puts "Ça fait combien 3 + 2 ? #{3 + 2}"

# Imprime sur l'écran Travail : et le résultat de l'opération entre #{} (soustraction)
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

# Imprime sur l'écran la phrase entre ""
puts "Ok, c'est faux alors !"

# Imprime sur l'écran la phrase entre ""
puts "C'est drôle ça, faisons-en plus :"

# Imprime sur l'écran Travail : et le résultat de l'opération entre #{} (valeur supérieure à une autre)
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"

# Imprime sur l'écran Travail : et le résultat de l'opération entre #{} (valeur supérieur ou égale à une autre)
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"

# Imprime sur l'écran Travail : et le résultat de l'opération entre #{} (valeur inférieure ou égale à une autre)
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"